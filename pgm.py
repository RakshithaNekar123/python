class Node:
    def __init__(self,data):
        self.data=data
        self.next=None
        self.prev=None
class doublylinkedlist:
    def __init__(self):
        self.head=None
        self.tail=None

    def insertatbeginning(self,new_data):
        new_node=Node(new_data)
        if self.head==None:
            self.head=new_node
            self.tail=new_node
            return
        
        new_node.next=self.head
        self.head.prev=new_node
        self.head=new_node

    def deleteatbegining(self):
        if self.head==None:
            print('empty')
            return
        if self.head==self.tail:
            self.head=None
            self.tail=None
            return
        self.head=self.head.next
        self.head.prev=None   

    def deleteatend(self,new_data):
        if self.head==None:
            self.head=self.head
             

    def insertatend(self,new_data):
        new_node=Node(new_data)
        if self.head==None:
            self.head=new_node
            self.tail=new_node
            return
        self.tail.next=new_node
        new_node.prev=self.tail
        self.tail=new_node
    def printlist(self):
        temp=self.head
        if temp==None:
            print('list is empty')
            return
        while temp:
            print(temp.data,end='')   
            temp=temp.next
        print()

